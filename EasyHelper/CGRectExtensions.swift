import CoreGraphics

// MARK: - Extensions CGRect, EasyHelper
extension CGRect: Hashable {
    

    /// Add HashValue For CGRect
    public var hashValue: Int {
        get {
            return (origin.x.hashValue ^ origin.y.hashValue ^ size.width.hashValue ^ size.height.hashValue)
        }
    }
}